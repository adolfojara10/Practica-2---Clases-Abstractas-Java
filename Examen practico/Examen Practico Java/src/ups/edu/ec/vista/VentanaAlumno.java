    private void tblAlumnosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlumnosMouseClicked
        // TODO add your handling code here:
        int fila = tblAlumnos.getSelectedRow();

        String nombres = (String) tblAlumnos.getValueAt(fila, 0);
        String[] nombre = nombres.split(" ", 2);

        txtNombre.setText(nombre[0]);
        txtApellido.setText(nombre[1]);
        txtFormattedCedula.setValue((String) tblAlumnos.getValueAt(fila, 1));
        cbxGenero.setSelectedItem((String) tblAlumnos.getValueAt(fila, 2));

        cursoAlumnoSeleccionado = (String) tblAlumnos.getValueAt(fila, 3);

        activarBotones();
    }//GEN-LAST:event_tblAlumnosMouseClicked



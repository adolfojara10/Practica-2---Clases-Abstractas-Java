    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String cedula = txtFormattedCedula.getText();

        String genero = (String) cbxGenero.getSelectedItem();

        if (nombre.isBlank() || apellido.isBlank() || cedula.isBlank() || genero.equals("--Seleccionar--")) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            alumno = new Alumno(null, controladorAlumno.generarID(), nombre, apellido, cedula, genero);
            if (controladorAlumno.create(alumno)) {
                JOptionPane.showMessageDialog(this, "Alumno creado con exito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                llenarTablaAlumnos();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Alumno ya existe", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String cedula = txtFormattedCedula.getText();

        String genero = (String) cbxGenero.getSelectedItem();

        if (nombre.isBlank() || apellido.isBlank() || cedula.isBlank() || genero.equals("--Seleccionar--")) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            Curso cur = null;
            if (cursoAlumnoSeleccionado != null) {
                try {
                    String[] opciones = cursoAlumnoSeleccionado.split("|");
                    cur = new Curso(Integer.valueOf(opciones[0].trim()), opciones[1], null, null, null);
                    cur = (Curso) controladorCurso.read(cur);
                } catch (NumberFormatException e) {
                    e.printStackTrace();
                }

                cursoAlumnoSeleccionado = null;
            } else {
                cur = null;
            }

            alumno = new Alumno(cur, controladorAlumno.generarID(), nombre, apellido, cedula, genero);
            if (controladorAlumno.update(alumno, alumno)) {
                JOptionPane.showMessageDialog(this, "Alumno creado con exito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                llenarTablaAlumnos();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Alumno ya existe", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        alumno = new Alumno(null, 0, txtNombre.getText(), txtApellido.getText(), (String) txtFormattedCedula.getValue(),
                (String) cbxGenero.getSelectedItem());

        controladorAlumno.delete(alumno);
        JOptionPane.showMessageDialog(this, "Alumno eliminado con exito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        limpiar();
        llenarTablaAlumnos();

    }//GEN-LAST:event_btnEliminarActionPerformed


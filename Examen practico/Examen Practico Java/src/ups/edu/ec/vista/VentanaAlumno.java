    public void llenarTablaAlumnos() {
        List<Alumno> listaAlumnos = (List<Alumno>) controladorAlumno.getListaGenerica();

        DefaultTableModel modelo = (DefaultTableModel) tblAlumnos.getModel();
        modelo.setRowCount(0);

        listaAlumnos.stream().map(var -> {
            String curso = "";
            if (var.getCurso() == null) {
                curso = "sin curso";
            } else {
                curso = var.getId() + "|" + var.getCurso().getNombreCurso();
            }
            Object[] row = {var.getNombre() + " " + var.getApellido(), var.getCedula(),
                var.getGenero(), curso};
            return row;
        }).forEachOrdered(row -> {
            modelo.addRow(row);
        });

        tblAlumnos.setModel(modelo);

    }


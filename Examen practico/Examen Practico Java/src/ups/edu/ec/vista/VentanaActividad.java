    public void llenarTablaAplicaciones(Set<String> aplicaciones) {
        aplicacionesActividad = aplicaciones;
        Iterator it = aplicaciones.iterator();

        System.out.println(aplicaciones);

        DefaultTableModel modelo = (DefaultTableModel) tblAplicacion.getModel();
        modelo.setRowCount(0);

        while (it.hasNext()) {
            String aplicacion = (String) it.next();
            Object[] row = {aplicacion};
            modelo.addRow(row);

        }

        tblAplicacion.setModel(modelo);
    }

    
    public void llenarTablaActividades() {
        List<Actividad> actividades = (List<Actividad>) controladorActividad.getListaGenerica();

        DefaultTableModel modelo = (DefaultTableModel) tblActividades.getModel();
        modelo.setRowCount(0);

        if (!actividades.isEmpty()) {
            for (Actividad var : actividades) {

                Object[] row = {var.getId(), var.getTitulo(), var.getAplicaciones()};
                modelo.addRow(row);

            }
        }

        tblActividades.setModel(modelo);

    }

    public void limpiar() {
        txtTitulo.setText("");

        DefaultTableModel modelo = (DefaultTableModel) tblAplicacion.getModel();
        modelo.setRowCount(0);
        tblAplicacion.setModel(modelo);
        cargarNumeroActividad();
        //llenarTablaActividades();

        btnEditar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnAnadir.setEnabled(true);
        btnBuscar.setEnabled(true);

        tblActividades.clearSelection();
    }

    public void cargarNumeroActividad() {
        txtActividadNumero.setText(String.valueOf(controladorActividad.generarID()));
    }


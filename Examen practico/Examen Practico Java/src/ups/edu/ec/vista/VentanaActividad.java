    private void tblActividadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblActividadesMouseClicked
        // TODO add your handling code here:
        int fila = tblActividades.getSelectedRow();

        txtActividadNumero.setText((String) tblActividades.getValueAt(fila, 0));
        txtTitulo.setText((String) tblActividades.getValueAt(fila, 1));
        Actividad acti = (Actividad) controladorActividad.read(new Actividad(Integer.valueOf((String) tblActividades.getValueAt(fila, 0)), (String) tblActividades.getValueAt(fila, 1)));
        llenarTablaAplicaciones(acti.getAplicaciones());

        btnEditar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnAnadir.setEnabled(false);
        btnBuscar.setEnabled(true);
    }//GEN-LAST:event_tblActividadesMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        String titulo = txtTitulo.getText();

        if (titulo.isBlank()) {
            JOptionPane.showMessageDialog(this, "Llene el campo título", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            actividad = controladorActividad.buscarActividad(titulo);
            actividad.setId(Integer.valueOf(txtActividadNumero.getText()));

            if (controladorActividad.update(actividad, actividad)) {
                JOptionPane.showMessageDialog(this, "Actividad actualiza con exito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
                llenarTablaActividades();
            } else {
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error", "Error", JOptionPane.ERROR_MESSAGE);
                limpiar();
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        Actividad actividad2 = new Actividad(Integer.valueOf(txtActividadNumero.getText()), txtTitulo.getText());

        controladorActividad.delete(actividad2);
        JOptionPane.showMessageDialog(this, "Actividad eliminada con exito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        limpiar();
        llenarTablaActividades();

    }//GEN-LAST:event_btnEliminarActionPerformed



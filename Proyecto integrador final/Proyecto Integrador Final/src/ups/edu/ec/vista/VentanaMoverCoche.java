    public void cargarVistaSitios() {
        
        int tamanioSitios = controladorSitio.findAll().size();
        
        var sitios = controladorSitio.findAll();
        
        for (int i = 0; i < tamanioSitios; i++) {
            sitio = sitios.get(i);
            JPanel panelito = listaPaneles.get(i);
            //panelito.setLayout(new BorderLayout());
            panelito.removeAll();
            JLabel labelSitio = new JLabel();
            
            switch (sitio.getEstado()) {
                case "DESOCUPADO" -> {
                    panelito.setBackground(Color.GREEN);
                    String texto = "<html><body> . <br> ? <br></body></html>";
                    texto = texto.replace(".", String.valueOf(sitio.getCodigo()));
                    texto = texto.replace("?", "Desocupado");
                    labelSitio.setText(texto);
                    labelSitio.setSize(100, 72);
                    
                    break;
                }
                case "OCUPADO" -> {
                    panelito.setBackground(Color.yellow);
                    String texto = "<html><body> . <br> ? <br></body></html>";
                    texto = texto.replace(".", String.valueOf(sitio.getCodigo()));
                    texto = texto.replace("?", "Ocupado");
                    labelSitio.setText(texto);
                    labelSitio.setSize(100, 72);
                    break;
                }
                
                default -> {
                    panelito.setBackground(Color.RED);
                    String texto = "<html><body> . <br> ? <br></body></html>";
                    texto = texto.replace(".", String.valueOf(sitio.getCodigo()));
                    texto = texto.replace("?", "Contratado");
                    labelSitio.setText(texto);
                    labelSitio.setSize(100, 72);
                    break;
                }
            }
            
            panelito.add(labelSitio, BorderLayout.CENTER);
            
            panelito.repaint();
            panelito.updateUI();
            panelito.revalidate();
            
        }
        
        int parqueaderosRestantes = listaPaneles.size() - tamanioSitios;
        
        for (int i = 0; i < parqueaderosRestantes; i++) {
            JPanel panel = listaPaneles.get(tamanioSitios + i);
            System.out.println(panel.getLocation());
            panel.setBackground(null);
            panel.removeAll();
            
        }
        
    }
    

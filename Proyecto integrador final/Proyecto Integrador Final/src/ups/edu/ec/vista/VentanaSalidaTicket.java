    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        int codigo = Integer.valueOf(txtCodigoBuscar.getText());
        //factura.setCodigo(codigo);

        factura = (Factura) controladorFactura.read(codigo);

        if (factura != null && factura.getFechaSalida() == null) {
            txtCodigoBuscar.setEditable(false);
            SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy - HH:mm:ss");
            Date d = factura.getFechaEntrada();
            String fechaEntrada;
            fechaEntrada = formatter.format(d);
            System.out.println(fechaEntrada);
            txtFechaEntrada.setText(fechaEntrada);
            cargarFecha();
            if (factura.getCedulaClienteFk() != null) {
                txtFormattedCedula.setValue(factura.getCedulaClienteFk().getCedula());
            } else {
                txtFormattedCedula.setValue("");
            }

            txtNumeroSitio.setText(String.valueOf(factura.getNumeroSitioFk().getCodigo()));
            btnCrear.setEnabled(true);
            cargarSubtotal();
            cargarTiempo();
        } else {
            JOptionPane.showMessageDialog(this, "Factura no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


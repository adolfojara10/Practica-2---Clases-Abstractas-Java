    public void cargarFecha() {
        try {
            Date fecha = new Date(Calendar.getInstance().getTimeInMillis());
            SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy - HH:mm:ss");

            String fechaTexto = formatter.format(fecha);

            txtFechaSalida.setText(fechaTexto);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void limpiar() {
        txtCodigoBuscar.setText("");
        txtCodigoBuscar.setEditable(true);

        txtFechaEntrada.setText("");
        txtNumeroSitio.setText("");
        txtSubtotal.setText("");
        txtTotal.setText("");
        txtFormattedCedula.setValue("");
        btnGroupDescuento.clearSelection();
        txtTiempo.setText("");
        btnCrear.setEnabled(false);
        cargarFecha();

    }

    public void cargarSubtotal() {
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy - HH:mm:ss");
        Date fechaSalida = null;
        try {
            fechaSalida = formatter.parse(txtFechaSalida.getText());
        } catch (ParseException ex) {
            Logger.getLogger(VentanaSalidaTicket.class.getName()).log(Level.SEVERE, null, ex);
        }
        BigDecimal subtotal = controladorFactura.calcularSubtotal(factura.getFechaEntrada(), fechaSalida);
        txtSubtotal.setText(String.valueOf(subtotal));
    }

    public void cargarTiempo() {
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy - HH:mm:ss");
        Date fechaSalida = null;
        try {
            fechaSalida = formatter.parse(txtFechaSalida.getText());
        } catch (ParseException ex) {
            Logger.getLogger(VentanaSalidaTicket.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtTiempo.setText(String.valueOf(controladorFactura.calcularTiempo(factura.getFechaEntrada(), fechaSalida)));
    }


    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:

        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy - HH:mm:ss");
        Date fechaSalida = new Date();
        try {
            fechaSalida = formatter.parse(txtFechaSalida.getText());
        } catch (ParseException ex) {

        }
        System.out.println("");
        factura.setFechaSalida(fechaSalida);
        factura.setSubtotal(BigDecimal.valueOf(Double.valueOf(txtSubtotal.getText())));
        factura.setTiempo(Integer.valueOf(txtTiempo.getText()));
        factura.setTotal(BigDecimal.valueOf(Double.valueOf(txtTotal.getText())));

        if (controladorFactura.update(factura)) {
            System.out.println(factura.getFechaEntrada() + " " + factura.getFechaSalida());
            JOptionPane.showMessageDialog(this, "Factura emitida con exito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            controladorSitio.cambiarAEstadoDesocupado(factura.getNumeroSitioFk());
            limpiar();
            ventanaVisualizarFactura.cargarFactura(factura);
            ventanaVisualizarFactura.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCrearActionPerformed


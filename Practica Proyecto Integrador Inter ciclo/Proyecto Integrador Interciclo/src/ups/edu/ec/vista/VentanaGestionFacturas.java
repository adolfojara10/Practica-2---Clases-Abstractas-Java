    public void llenarTblFacturas() {

        DefaultTableModel modelo = (DefaultTableModel) tblFacturas.getModel();
        modelo.setRowCount(0);

        for (Factura fac : (List<Factura>) controladorFactura.getListaGenerica()) {
            
            String informacionCliente = "";
            if(fac.getCliente() != null){
                informacionCliente = fac.getCliente().getCedula() + " " + fac.getCliente().getApellido();
            } else {
                informacionCliente = "null";
            }
            if (fac.getFechaSalida()!= null) {
                Object[] row = {fac.getCodigo(), fac.getFechaEntrada().toString(), fac.getFechaSalida().toString(),
                    fac.getTiempo(), fac.getSubtotal(), fac.getTotal(),
                    informacionCliente,
                    fac.getSitio().getCodigo()};

                modelo.addRow(row);
            } 

        }

        tblFacturas.setModel(modelo);

    }

    public void llenarTblFacturaEncontrada(Factura factura) {
        DefaultTableModel modelo = (DefaultTableModel) tblFacturas.getModel();
        modelo.setRowCount(0);

        Object[] row = {factura.getCodigo(), factura.getFechaEntrada().toString(),
            factura.getFechaSalida().toString(), factura.getTiempo(), factura.getSubtotal(),
            factura.getTotal(), factura.getCliente().getCedula() + " " + factura.getCliente().getApellido(),
            factura.getSitio().getCodigo()};

        modelo.addRow(row);

        tblFacturas.setModel(modelo);
    }

    public void limpiar() {
        txtBuscar.setText("");
        llenarTblFacturas();

    }


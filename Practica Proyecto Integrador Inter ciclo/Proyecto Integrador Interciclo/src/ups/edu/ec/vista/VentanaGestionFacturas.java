    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigoBusqueda = txtBuscar.getText();

        if (codigoBusqueda.isBlank() || codigoBusqueda.contains("\\D")) {
            JOptionPane.showMessageDialog(this, "Llene el campo busqueda", "Error", JOptionPane.ERROR_MESSAGE);

        } else {

            int cod = Integer.valueOf(codigoBusqueda);
            factura.setCodigo(cod);
            factura.setTiempo(0);
            factura = (Factura) controladorFactura.read(factura);
            if (factura != null) {
                llenarTblFacturaEncontrada(factura);
            } else {
                JOptionPane.showMessageDialog(this, "Factura no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
                limpiar();
            }

        }
    }//GEN-LAST:event_btnBuscarActionPerformed


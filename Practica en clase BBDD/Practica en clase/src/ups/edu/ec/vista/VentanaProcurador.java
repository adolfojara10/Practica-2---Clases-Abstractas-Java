    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:

        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();

        Date fechaNacimiento = jDateChooser1.getDate();

        int numeroExpediente = Integer.valueOf(txtNumeroExpediente.getText());

        if (cedula.isBlank() || nombre.isBlank() || apellido.isBlank() || direccion.isBlank()
                || telefono.isBlank()) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

            asunto = controladorAsunto.findAsunto(numeroExpediente);

            if (asunto != null) {
                procurador = new Procurador(controladorProcurador.getProcuradorCount(), nombre, apellido, telefono, fechaNacimiento);

                procurador.setCedula(cedula);
                procurador.setNumeroAsuntoFk(asunto);
                procurador.setDireccion(direccion);
                
                
                try {
                    controladorProcurador.create(procurador);

                    JOptionPane.showMessageDialog(this, "Procurador creado con exito", "Informacion", JOptionPane.INFORMATION_MESSAGE);

                    btnFin.setEnabled(true);
                } catch (Exception ex) {
                    //Logger.getLogger(VentanaCliente.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "El procurador ya existe", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }

        }
    }//GEN-LAST:event_btnCrearActionPerformed


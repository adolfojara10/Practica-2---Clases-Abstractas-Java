    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        String cedula = txtCedula.getText();

        procurador = controladorProcurador.findByCedula(cedula);

        if (procurador != null) {
            txtNombre.setText(procurador.getNombre());
            txtApellido.setText(procurador.getApellido());
            txtDireccion.setText(procurador.getDireccion());
            txtTelefono.setText(procurador.getTelefono());
            jDateChooser1.setDate(procurador.getFechaNacimiento());
            btnFin.setEnabled(true);
            //btnActualizar.setEnabled(true);
            txtCedula.setEnabled(false);
            //btnEliminar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "El cliente no existe", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


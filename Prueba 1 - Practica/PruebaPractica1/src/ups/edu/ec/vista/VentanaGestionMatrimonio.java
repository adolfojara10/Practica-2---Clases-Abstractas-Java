    public void llenarTblMatrimonios() {
        List<Matrimonio> listaMatrimonios = (List<Matrimonio>) controladorMatrimonio.getListaGenerica();

        DefaultTableModel modelo = (DefaultTableModel) tblMatrimonios.getModel();
        modelo.setRowCount(0);

        for (Matrimonio matri : listaMatrimonios) {
            List<Conyuge> listaConyuges = matri.getListaConyuges();
            String nombres = "";

            for (Conyuge con : listaConyuges) {
                nombres += con.getNombre() + " " + con.getApellido();
                nombres += ", ";
            }

            String testigos = "";
            for (Testigo tes : matri.getListaTestigo()) {
                testigos += tes.getNombre() + " " + tes.getApellido();
                testigos += ", ";
            }

            String juez = matri.getJuez().getNombre() + " " + matri.getJuez().getApellido();

            Object[] row = {matri.getNumeroMatrimonio(), matri.getFechaMatrimonio().toString(),
                nombres, testigos, juez};

            modelo.addRow(row);
        }

        tblMatrimonios.setModel(modelo);
    }

    public void llenarMatrimonioBuscado(Matrimonio matri) {
        DefaultTableModel modelo = (DefaultTableModel) tblMatrimonios.getModel();
        modelo.setRowCount(0);
        
        List<Conyuge> listaConyuges = matri.getListaConyuges();
        String nombres = "";

        for (Conyuge con : listaConyuges) {
            nombres += con.getNombre() + " " + con.getApellido();
            nombres += "\n";
        }

        String testigos = "";
        for (Testigo tes : matri.getListaTestigo()) {
            testigos += tes.getNombre() + " " + tes.getApellido();
            testigos += "\n";
        }

        String juez = matri.getJuez().getNombre() + " " + matri.getJuez().getApellido();

        Object[] row = {matri.getNumeroMatrimonio(), matri.getFechaMatrimonio().toString(),
            nombres, testigos, juez};

        modelo.addRow(row);

        tblMatrimonios.setModel(modelo);

    }


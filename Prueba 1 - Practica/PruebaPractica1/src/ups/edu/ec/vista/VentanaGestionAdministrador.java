    public void llenarTblAdministradores() {
        DefaultTableModel modelo = (DefaultTableModel) tblAdministradores.getModel();
        modelo.setRowCount(0);

        for (Administrador admin : (List<Administrador>) controladorAdministrador.getListaGenerica()) {

            String calles = admin.getDireccion().getCalle1() + "  " + 
                    admin.getDireccion().getCalle2() + "  " + admin.getDireccion().getNumeroCalle();
            
            Object[] row = {admin.getNombre(), admin.getApellido(), admin.getCedula(),
                admin.getFechaNacimiento().toString(), admin.getEmail(), admin.getPassword(),
                admin.getGenero(), calles};

            modelo.addRow(row);
        }

        tblAdministradores.setModel(modelo);

    }


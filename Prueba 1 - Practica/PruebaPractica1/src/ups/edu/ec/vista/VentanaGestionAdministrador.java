                admin.setApellido(apellido);
                admin.setCedula(cedula);
                admin.setGenero(genero);
                admin.setFechaNacimiento(fechaNacimiento);
                admin.setDireccion(direccion);
                admin.setEmail(email);
                admin.setPassword(password);

                controladorAdministrador.update(admin, admin);
                JOptionPane.showMessageDialog(this, "Administrador actualizado con exito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
